    private void btnAccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccionActionPerformed
        if (accion.equals("Registrar")) {

            if (txtNombre.getText().isEmpty() || txtAbreviatura.getText().isEmpty()) {
                ErrorAlerta ae = new ErrorAlerta(null, true);
                ErrorAlerta.lblMensaje2.setText("¡¡¡COMPLETE TODOS LOS CAMPOS!!!");
                ae.setVisible(true);

                txtNombre.requestFocus();
            } else {
                u.setUnidad(txtNombre.getText());
                u.setAbreviatura(txtAbreviatura.getText());
                u.setDescripcion(txtDescripcion.getText());

                if (uc.insertUnidad(u)) {

                    ExitoAlerta ae = new ExitoAlerta(null, true);
                    ExitoAlerta.lblMensaje1.setText("¡¡¡UNIDAD DE MEDIDA!!!");
                    ExitoAlerta.lblMensaje2.setText("¡¡¡REGISTRADA CON EXITO!!!");
                    ae.setVisible(true);
                    dispose();

                } else {
                    ErrorAlerta ae = new ErrorAlerta(null, true);
                    ErrorAlerta.lblMensaje2.setText("¡¡¡UNIDAD NO REGISTRADA!!!");
                    ae.setVisible(true);
                }

            }
        } else {

            if (txtNombre.getText().isEmpty() || txtAbreviatura.getText().isEmpty()) {
                ErrorAlerta ae = new ErrorAlerta(null, true);
                ErrorAlerta.lblMensaje2.setText("¡¡¡COMPLETE TODOS LOS CAMPOS!!!");
                ae.setVisible(true);

                txtNombre.requestFocus();
            } else {
                u.setIdUnidad(unidad.getIdUnidad());
                u.setUnidad(txtNombre.getText());
                u.setAbreviatura(txtAbreviatura.getText());
                u.setDescripcion(txtDescripcion.getText());

                if (uc.updateUnidad(u)) {

                    ExitoAlerta ae = new ExitoAlerta(null, true);
                    ExitoAlerta.lblMensaje1.setText("¡¡¡UNIDAD DE MEDIDA!!!");
                    ExitoAlerta.lblMensaje2.setText("¡¡¡ACTUALIZADA CON EXITO!!!");
                    ae.setVisible(true);
                    dispose();

                } else {
                    ErrorAlerta ae = new ErrorAlerta(null, true);
                    ErrorAlerta.lblMensaje2.setText("¡¡¡UNIDAD NO ACTUALIZADA!!!");
                    ae.setVisible(true);
                }

            }

        }
    }//GEN-LAST:event_btnAccionActionPerformed

